package modelo;

public class DocumentoTipo {

    private int IDTIPDOC;
    private String TIPDOC;
    private String NOMTIPDOC;
    private String ESTTIPDOC;

    public int getIDTIPDOC() {
        return IDTIPDOC;
    }

    public void setIDTIPDOC(int IDTIPDOC) {
        this.IDTIPDOC = IDTIPDOC;
    }

    public String getTIPDOC() {
        return TIPDOC;
    }

    public void setTIPDOC(String TIPDOC) {
        this.TIPDOC = TIPDOC;
    }

    public String getNOMTIPDOC() {
        return NOMTIPDOC;
    }

    public void setNOMTIPDOC(String NOMTIPDOC) {
        this.NOMTIPDOC = NOMTIPDOC;
    }

    public String getESTTIPDOC() {
        return ESTTIPDOC;
    }

    public void setESTTIPDOC(String ESTTIPDOC) {
        this.ESTTIPDOC = ESTTIPDOC;
    }
}
