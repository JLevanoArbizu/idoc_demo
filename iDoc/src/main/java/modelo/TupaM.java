package modelo;

public class TupaM {

    private String CODTUP;
    private String NUMTUP;
    private String NOMTUP;
    private double PRETUP;
    private String PLATUP;
    private String ARETUP;

    public String getCODTUP() {
        return CODTUP;
    }

    public void setCODTUP(String CODTUP) {
        this.CODTUP = CODTUP;
    }

    public String getNUMTUP() {
        return NUMTUP;
    }

    public void setNUMTUP(String NUMTUP) {
        this.NUMTUP = NUMTUP;
    }

    public String getNOMTUP() {
        return NOMTUP;
    }

    public void setNOMTUP(String NOMTUP) {
        this.NOMTUP = NOMTUP;
    }

    public double getPRETUP() {
        return PRETUP;
    }

    public void setPRETUP(double PRETUP) {
        this.PRETUP = PRETUP;
    }

    public String getPLATUP() {
        return PLATUP;
    }

    public void setPLATUP(String PLATUP) {
        this.PLATUP = PLATUP;
    }

    public String getARETUP() {
        return ARETUP;
    }

    public void setARETUP(String ARETUP) {
        this.ARETUP = ARETUP;
    }
}